export function normalizeLocale(input?: string | null): AppLocale {
  const value = (input ?? "").toLowerCase();
  const matched = LOCALE_ALIASES.find(([prefix]) => value.startsWith(prefix));
  return matched?.[1] ?? "en";
}

export function translate(locale: AppLocale, key: string, vars?: Record<string, string | number>) {
  const template = locale === "en" ? key : translations[locale][key] ?? key;
  if (!vars) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ""));
}

export function formatDateTime(locale: AppLocale, timestamp: number) {
  const intlLocale =
    locale === "zh" ? "zh-CN" : locale === "pt" ? "pt-BR" : locale === "en" ? "en-AU" : locale;

  return new Intl.DateTimeFormat(intlLocale, {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(timestamp);
}

export interface I18nContextValue {
  locale: AppLocale;
  setLocale: (locale: AppLocale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  formatDateTime: (timestamp: number) => string;
}

export const I18nContext = createContext<I18nContextValue | null>(null);

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }

  return context;
}
